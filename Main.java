public class Main {
    public static void main(String[] args) {
        // atribute
        int a, b;
        int hasilJumlah, hasilKurang, hasilKali;
        a = 10;
        b = 20;
        // konstruktor
        hasilJumlah = a + b;
        hasilKurang = a - b;
        hasilKali = a * b;
        System.out.print(
                "\nHasil Semua Perhitungan adalah \t" + "\nJumlah\t=" + hasilJumlah + "\nKurang\t=" + hasilKurang
                        + "\nKali\t=" + hasilKali);
    }
}
